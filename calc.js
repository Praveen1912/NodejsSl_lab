function add(val1,val2)
{
    
    return val1+val2
}
function sub(val1,val2)
{
    return val1-val2
}
function mul(val1,val2)
{
    return val1*val2
}
function div(val1,val2)
{
    return val1/val2
}
module.exports.add=add
module.exports.sub=sub
module.exports.mul=mul
module.exports.div=div
